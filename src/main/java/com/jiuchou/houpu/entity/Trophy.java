package com.jiuchou.houpu.entity;


public class Trophy {

  private String pid;
  private String pname;
  private String pintro;
  private String pimgUrl;
  private String heartContent;
  private String title;
  private String info;
  private String pintrotitle;
  private long sort;
  private String common;


  public String getPid() {
    return pid;
  }

  public void setPid(String pid) {
    this.pid = pid;
  }


  public String getPname() {
    return pname;
  }

  public void setPname(String pname) {
    this.pname = pname;
  }


  public String getPintro() {
    return pintro;
  }

  public void setPintro(String pintro) {
    this.pintro = pintro;
  }


  public String getPimgUrl() {
    return pimgUrl;
  }

  public void setPimgUrl(String pimgUrl) {
    this.pimgUrl = pimgUrl;
  }


  public String getHeartContent() {
    return heartContent;
  }

  public void setHeartContent(String heartContent) {
    this.heartContent = heartContent;
  }


  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }


  public String getInfo() {
    return info;
  }

  public void setInfo(String info) {
    this.info = info;
  }


  public String getPintrotitle() {
    return pintrotitle;
  }

  public void setPintrotitle(String pintrotitle) {
    this.pintrotitle = pintrotitle;
  }


  public long getSort() {
    return sort;
  }

  public void setSort(long sort) {
    this.sort = sort;
  }


  public String getCommon() {
    return common;
  }

  public void setCommon(String common) {
    this.common = common;
  }

}
